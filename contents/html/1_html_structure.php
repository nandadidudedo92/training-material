      <div align="left">
      <p> <strong>HyperText Markup Language (HTML)</strong> adalah suatu bahasa yang menggunakan tanda-tanda tertentu (tag) untuk menyatakan kode-kode yang harus ditafsirkan oleh browser agar halaman tersebut dapat ditampilkan secara benar.</p>
      <p>Masing-masing halaman <strong>HTML</strong> terdiri atas seperangkat <strong>tags</strong> (bisa disebut juga<strong> elements</strong>), yang mengacu pada building block halaman website. Tag tersebut membuat hirarki yang menyusun konten hingga menjadi bagian, paragraf, heading, dan block konten lainnya.</p>
      <p>Sebagian besar element HTML memiliki tag pembuka dan penutup yang menggunakan syntax <strong>&lt;tag&gt;&lt;/tag&gt;</strong>.</p>
    <ul>
         <li><code><!DOCTYPE html></code> Tag paling awal dari contoh HTML diatas adalah <strong>DTD</strong> atau <strong>DOCTYPE</strong></li>
         <li><strong>DTD </strong> dalah singkatan dari <strong>Document Type Declaration</strong>. Yang berfungsi untuk memberi tahu kepada web browser bahwa dokumen yang akan diproses adalah HTML.</li>
         <li><strong>&lt;html&gt </strong> adalah tag pembuka dari keseluruhan halaman web. Semua kode&nbsp;<strong>HTML</strong>&nbsp;harus berada di dalam tag ini. Tag html dimulai dengan&nbsp;<strong>&lt;html&gt;</strong>&nbsp;dan diakhiri dengan&nbsp;<strong>&lt;/html&gt;</strong></li>
         <li><strong>&lt;head&gt </strong> Elemen pada tag&nbsp;<strong>&lt;head&gt;</strong>&nbsp;umumnya akan berisi berbagai definisi halaman, seperti kode&nbsp;<strong>CSS</strong>,&nbsp;<strong>JavaScript</strong>, dan kode-kode lainnya yang tidak tampil di browser.</li>
        <li><strong>&lt;title&gt; </strong> Tag&nbsp;<strong>&lt;title&gt;</strong>&nbsp;dalam contoh kita sebelumnya digunakan untuk menampilkan title dari sebuah halaman web.&nbsp; </li>
       <li><strong> &lt;body&gt;</strong> Tag&nbsp;<strong>&lt;body&gt;</strong>&nbsp;akan berisi semua elemen yang akan tampil dalam halaman web, seperti paragraf, tabel, link, gambar, dll. Tag body ini ditutup dengan&nbsp;<strong>&lt;/body&gt;.&nbsp;</strong>Sebagian besar waktu kita dalam merancang web akan dihabiskan di dalam tag &lt;body&gt; ini</li>
       <li><strong> &lt;body&gt;</strong> Tag&nbsp;<strong>&lt;body&gt;</strong>&nbsp;akan berisi semua elemen yang akan tampil dalam halaman web, seperti paragraf, tabel, link, gambar, dll. Tag body ini ditutup dengan&nbsp;<strong>&lt;/body&gt;.&nbsp;</strong>Sebagian besar waktu kita dalam merancang web akan dihabiskan di dalam tag &lt;body&gt; ini</li>        
